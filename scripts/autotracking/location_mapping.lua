---@param room number?
---@param flagByte number?
---@param bitMask number?
---@param id number?
---@return number
function LocationID(room, flagByte, bitMask, id)
	if id then
		return id
	end
	if not flagByte then
		if room >= 0x200 then
			if room >= 0x300 then
				if room >= 0x600 then
					room = room - 0x200
				end
				room = room - 0x100
			end
			room = room - 0x100
		end
		flagByte = 0xc700 + room
	end
	return flagByte * 0x100 + (bitMask and bitMask or 0x20)
end
